
safe_check_packages <- function(..., args, quiet) {

  lib <- paste(.libPaths(), collapse = .Platform$path.sep)

  with_envvar(
    c(R_LIBS = lib,
      R_LIBS_USER = lib,
      R_LIBS_SITE = lib,
      R_PROFILE_USER = tempfile()),
    R("CMD", "check", ..., args, callback = if (!quiet) check_callback())
  )
}
